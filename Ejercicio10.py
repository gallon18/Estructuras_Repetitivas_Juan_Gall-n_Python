# Escribir un programa que multiplique los 20 primeros números naturales. Ejemplo:

# Inicializamos la variable que almacenará el resultado
resultado = 1

# Multiplicamos los 20 primeros números naturales
for i in range(1, 21):
    resultado *= i

# Imprimimos el resultado
print("El producto de los 20 primeros números naturales es:", resultado)